# Operadores de Comparação

saldo = 200
saque = 200

print(f"Saque é igual ao saldo = {saque == saldo}")
print(f"Saque é diferente do saldo = {saque != saldo}")
print(f"Saque é maior do que o saldo = {saque > saldo}")
print(f"Saque é maior ou igual ao saque = {saldo >= saque}")
print(f"Saque é menor do que o saldo = {saque < saldo}")
print(f"Saque é menor ou igual ao saldo = {saque <= saldo}")
