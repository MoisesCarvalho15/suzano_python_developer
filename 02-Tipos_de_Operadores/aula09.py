# Operadores de Associação

cursos = ["Python", "JavaScript", "Java", "PHP"]

print(f"C# está na lista? {"R" in cursos}")
print(f"Python está na lista? {"Python" in cursos}")
print(f"JavaScript não está na lista? {"JavaScript" not in cursos}")

# Case-sensitive
print(f"python está na lista? {"python" in cursos}")
