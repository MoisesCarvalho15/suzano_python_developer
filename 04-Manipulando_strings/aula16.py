# Fatiamento de Strings
# start, stop, step

#índice 012345...
nome = "Moisés Santos Carvalho"

# Pegando um caractere exato na string
print(f"Terceiro caractere: {nome[2]}")

# Pegando até um índice exato
print(nome[:6])

# Pegando o restante dos caracteres a partir de um outro
print(nome[7:])

# Invertendo os dados da string
print(nome[::-1])

# Pegando a string inteira e pulando de 2 em 2 caracteres
print(nome[::2])
